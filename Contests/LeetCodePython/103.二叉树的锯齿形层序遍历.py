from collections import deque
from typing import List, Optional


class TreeNode:
    def __init__(self, val=0, left=None, right=None):
        self.val = val
        self.left = left
        self.right = right
# @lc code=start
# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, val=0, left=None, right=None):
#         self.val = val
#         self.left = left
#         self.right = right
class Solution:
    def zigzagLevelOrder(self, root: Optional[TreeNode]) -> List[List[int]]:
        if root is None:
            return []

        ord = 0
        ans = []
        q = [root]
        while q:
            tmp = q
            q = []
            res = deque()
            for x in tmp:
                if ord:
                    res.appendleft(x.val)
                else:
                    res.append(x.val)
                if x.left:
                    q.append(x.left)
                if x.right:
                    q.append(x.right)
                    
            ans.append(list(res))
            ord ^= 1
        return ans
# @lc code=end

