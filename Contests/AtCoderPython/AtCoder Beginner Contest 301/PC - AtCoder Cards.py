def solve() -> None:
    s = input()
    t = input()
    cnt = Counter(s)
    at2 = 0
    for c in t:
        if c != "@":
            if cnt[c]:
                cnt[c] -= 1
            elif c in "atcoder" and cnt["@"]:
                cnt["@"] -= 1
            else:
                print("No")
                return
        else:
            at2 += 1
    for k, v in cnt.items():
        if v and (k not in "atcoder@" or v > at2):
            print("No")
            return
        at2 -= v
    print("Yes")

solve()