def solve() -> None:
    n, m = mint()
    s = []
    x1 = y1 = math.inf
    x2 = y2 = -math.inf
    for i in range(n):
        s.append(list(input()))
        for j in range(m):
            if s[-1][j] == "#":
                x1 = min(x1, i)
                y1 = min(y1, j)
                x2 = max(x2, i)
                y2 = max(y2, j)
    
    for i in range(x1, x2 + 1):
        for j in range(y1, y2 + 1):
            if s[i][j] == ".":
                print(i + 1, j + 1)
                return

solve()