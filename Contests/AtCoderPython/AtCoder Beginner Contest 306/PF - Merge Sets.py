class BIT:
    def __init__(self, n: int):
        self.nums = [0] * (n + 1)
        self.n = n
        self.BITree = [0] * (self.n + 1)
        
    def lowbit(self, x: int) -> int:
        return x & -x
    
    def query(self, x: int) -> int:
        ans = 0
        while x:
            ans += self.BITree[x]
            x -= self.lowbit(x)
        return ans

    def add(self, x: int, val: int):
        while x <= self.n:
            self.BITree[x] += val
            x += self.lowbit(x)

    def update(self, x: int, val: int) -> None:
        self.add(x + 1, val - self.nums[x])
        self.nums[x] = val

def solve() -> None:
    n, m = mint()
    s = []
    nums = []
    for _ in range(n):
        s.append(ints())
        s[-1].sort()
        nums.extend(s[-1])
    bit = BIT(len(nums))
    nums.sort()
    d = {v:i for i, v in enumerate(nums)}
    ans = 0
    for i in range(n - 1, -1, -1):
        for j, v in enumerate(s[i], 1):
            ans += j * (n - 1 - i) + bit.query(d[v]) - j + 1
            bit.update(d[v], 1)

    print(ans)

