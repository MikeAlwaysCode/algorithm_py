def solve() -> None:
    n, s = mint()
    nums = ints()
    ans = 0
    dp = [0] * (s + 1)
    for x in nums:
        dp[0] += 1
        for i in range(s, x - 1, -1):
            dp[i] = (dp[i] + dp[i - x]) % MOD
        ans = (ans + dp[s]) % MOD
    print(ans)

solve()