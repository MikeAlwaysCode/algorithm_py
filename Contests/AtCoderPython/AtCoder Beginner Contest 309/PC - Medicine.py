def solve() -> None:
    n, k = mint()
    pill = []
    for _ in range(n):
        pill.append(tuple(mint()))
    pill.sort(reverse = True)
    ans = last = pill[0][0] + 1
    s = 0
    for a, b in pill:
        if a != last:
            if s <= k:
                ans = a + 1
            else:
                break
        s += b
        last = a
    if s <= k: ans = 1
    print(ans)
        

solve()