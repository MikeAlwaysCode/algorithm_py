def solve() -> None:
    n, m = mint()
    A = ints()
    B = ints()
    
    nums = []
    for x in A:
        nums.append((x, 0, -1))
    for x in B:
        nums.append((x, 1, 1))
    
    nums.sort()
    ans = nums[-1][0] + 1
    cnt = [n, 0]
    for i in range(len(nums) - 1, -1, -1):
        cnt[nums[i][1]] += nums[i][2]
        if nums[i][1] == 0: ans = nums[i][0]
        else: ans = min(ans, nums[i][0] + 1)
        if cnt[0] < cnt[1]: break

    print(ans)


solve()