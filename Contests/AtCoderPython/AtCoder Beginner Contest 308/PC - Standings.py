def solve() -> None:
    n = sint()
    p = []
    for _ in range(n):
        p.append(tuple(mint()))
    ans = list(range(1, n + 1))
    def comp(x, y):
        return - p[y - 1][1] * (p[x - 1][0] + p[x - 1][1]) + p[x - 1][1] * (p[y - 1][0] + p[y - 1][1])
    ans.sort(key = cmp_to_key(comp))
    print(*ans)

