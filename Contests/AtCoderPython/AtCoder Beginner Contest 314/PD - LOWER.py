def solve() -> None:
    n = sint()
    s = list(input())
    t = [0] * n
    last = lt = 0
    for i in range(sint()):
        qry = input().split()
        if qry[0] == "1":
            j = int(qry[1]) - 1
            s[j] = qry[2]
            t[j] = i
        else:
            last = int(qry[0])
            lt = i
    
    if last == 2:
        s = [c.lower() if t[i] <= lt else c for i, c in enumerate(s)]
    elif last == 3:
        s = [c.upper() if t[i] <= lt else c for i, c in enumerate(s)]
    print("".join(s))

solve()