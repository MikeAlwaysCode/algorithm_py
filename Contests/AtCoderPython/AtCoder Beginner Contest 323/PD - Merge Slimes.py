def solve() -> None:
    n = sint()
    cnt = Counter()
    nums = []
    ans = 0
    for _ in range(n):
        s, c = mint()
        cnt[s] = c
        nums.append(s)
    
    nums.sort()
    for x in nums:
        if cnt[x] == 0: continue
        c = cnt[x]
        while c:
            ans += c & 1
            c //= 2
            x *= 2
            c += cnt[x]
            cnt[x] = 0
    print(ans)

solve()