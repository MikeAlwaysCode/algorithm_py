def solve() -> None:
    n, m = mint()
    s = []
    for _ in range(n):
        s.append(input())
    
    valid = [[False] * n for _ in range(n)]
    for i in range(n):
        for j in range(i + 1, n):
            cnt = 0
            for k in range(m):
                if s[i][k] != s[j][k]: cnt += 1
            if cnt == 1:
                valid[i][j] = valid[j][i] = True
    
    for p in permutations(range(n), n):
        if all(valid[p[i]][p[i + 1]] for i in range(n - 1)):
            print("Yes")
            return
    
    print("No")

solve()