def solve() -> None:
    n, m, h = mint()
    res = []
    for _ in range(n):
        t = ints()
        t.sort()
        s = c = p = 0
        for x in t:
            s += x
            if s > h: break
            c += 1
            p += s
        res.append((c, p))
    idx = sorted(range(n), key = lambda x: (-res[x][0], res[x][1]))
    print(idx.index(0) + 1)

for _ in range(int(input())):
    solve()