def solve() -> None:
    x, n = mint()
    primes = []
    p = 2
    while p * p <= x:
        if x % p == 0:
            primes.append(p)
            while x % p == 0: x //= p
        p += 1
    if x > 1: primes.append(x)

    ans = 1
    for p in primes:
        power = 0
        factor = p
        while factor <= n:
            power += n // factor
            factor *= p
        ans *= pow(p, power, MOD)
        ans %= MOD
    print(ans)

solve()