def solve() -> None: 
    n = sint()
    nums = ints()
    cnt = Counter()
    ans = l = k = 0
    for r in range(n):
        cnt[nums[r]] += 1
        if cnt[nums[r]] == 1: k += 1
        while k > 2:
            cnt[nums[l]] -= 1
            if cnt[nums[l]] == 0: k -= 1
            l += 1
        ans = max(ans, r - l + 1)
    print(ans)   

solve()