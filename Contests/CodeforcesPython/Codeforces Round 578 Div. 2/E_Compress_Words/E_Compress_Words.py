def prefix_function(s):
    n = len(s)
    pi = [0] * n
    for i in range(1, n):
        j = pi[i - 1]
        while j > 0 and s[i] != s[j]:
            j = pi[j - 1]
        if s[i] == s[j]:
            j += 1
        pi[i] = j
    return pi

def solve() -> None:
    n = sint()
    s = input().split()
    ans = list(s[0])
    for i in range(1, n):
        m = min(len(ans), len(s[i]))
        pi = prefix_function(s[i][:m] + '#' + "".join(ans[-m:]))
        ans.extend(s[i][pi[-1]:])
    print("".join(ans))

solve()
