def solve() -> None:
    n, k = mint()
    a = ints()
    h = ints()
    right = list(range(n))
    cnt = a[-1]
    ans, j = 1, n - 1
    if cnt > k:
        ans = cnt = 0
        j = n - 2
    for i in range(n - 2, -1, -1):
        if h[i] % h[i + 1] == 0:
            right[i] = right[i + 1]
        cnt += a[i]
        while j > right[i] or cnt > k:
            cnt -= a[j]
            j -= 1
        ans = max(ans, j - i + 1)
    print(ans)

for _ in range(int(input())):
    solve()