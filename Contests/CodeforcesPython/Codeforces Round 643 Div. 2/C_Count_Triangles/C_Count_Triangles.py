def solve() -> None:
    a, b, c, d = mint()
    # x + y > z
    # count x + y
    mx = 10 ** 6 + 2
    cnt = [0] * mx
    for x in range(a, b + 1):
        cnt[x + b] += 1
        cnt[x + c + 1] -= 1
    for i in range(1, mx):
        # 差分前缀和 -> 原
        cnt[i] += cnt[i - 1]
    for i in range(1, mx):
        # 原前缀和
        cnt[i] += cnt[i - 1]
    ans = 0
    for z in range(c, d + 1):
        ans += cnt[-1] - cnt[z]
    print(ans)

solve()