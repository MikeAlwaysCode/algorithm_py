def solve() -> None:
    n, k = mint()
    nums = ints()

    def check(x: int) -> bool:
        for i in range(n):
            m = k
            for j in range(i, n):
                t = x - j + i
                m -= max(0, t - nums[j])
                if m < 0: break
                if t <= nums[j] or (j < n - 1 and t <= nums[j + 1] + 1): return True
        return False

    l = max(nums)
    r = l + k
    while l < r:
        mid = l + r + 1 >> 1
        if check(mid):
            l = mid
        else:
            r = mid - 1
    print(l)

for _ in range(int(input())):
    solve()