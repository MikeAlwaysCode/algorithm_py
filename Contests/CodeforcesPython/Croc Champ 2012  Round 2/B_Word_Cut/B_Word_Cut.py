def solve() -> None:
    s = input()
    t = input()
    k = sint()
    n, m = len(s), 0
    ss = s + s
    for i in range(n):
        # if s[i:] + s[:i] == t: m += 1
        if ss[i:i + n] == t: m += 1
    if m == 0:
        print(0)
        return
    dp = [0] * 2
    dp[int(s==t)] = 1
    l = n - m
    for _ in range(k):
        dp[0], dp[1] = (dp[0] * (l - 1) + dp[1] * l) % MOD, (dp[0] * m + dp[1] * (m - 1)) % MOD
    print(dp[1])

solve()