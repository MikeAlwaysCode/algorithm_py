factor = [1] * (100)
primes = set()
def init():
    for i in range(2, 100):
        if factor[i] != 1:
            continue
        primes.add(i)
        for j in range(i, 100, i):
            factor[j] = i

def solve() -> None:
    s = input()
    for i in range(len(s)):
        for j in range(i + 1, len(s)):
            d = int(s[i]) * 10 + int(s[j])
            if d in primes:
                print(d)
                return
    print(-1)

init()
for _ in range(int(input())):
    solve()