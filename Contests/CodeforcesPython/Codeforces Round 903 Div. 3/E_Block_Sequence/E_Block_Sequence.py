def solve() -> None:
    n = sint()
    nums = ints()
    dp = [0] * (n + 1)
    for i in range(n - 1, -1, -1):
        dp[i] = dp[i + 1] + 1
        if nums[i] + i + 1 <= n:
            dp[i] = min(dp[i], dp[nums[i] + i + 1])
    print(dp[0])

for _ in range(int(input())):
    solve()