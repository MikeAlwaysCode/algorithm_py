def solve() -> None:
    n = sint()
    s = input()

    if n & 1:
        print(-1)
        return

    k = 1
    ans = [1] * n

    cur = 0
    p = 1
    t = 1 if s[0] == "(" else -1
    for i, c in enumerate(s):
        if c == "(":
            cur += 1
        else:
            cur -= 1
        
        if cur * t < 0:
            t = - t
            p ^= 3
            if k == 1: k += 1
        
        ans[i] = p
    
    if cur != 0:
        print(-1)
    else:
        print(k)
        print(*ans)

for _ in range(int(input())):
    solve()