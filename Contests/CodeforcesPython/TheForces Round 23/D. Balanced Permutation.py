def solve() -> None:
    n = sint()
    target = n * (n + 1) // 4
    left = set()
    s, i = 0, 1
    while i <= n and s + i <= target:
        left.add(i)
        s += i
        i += 1
    d = target - s
    r = n
    while d:
        i -= 1
        while r in left:
            r -= 1
        m = min(d, r - i)
        d -= m
        left.add(i + m)
        left.remove(i)
    ans = sorted(left)
    for i in range(1, n + 1):
        if i not in left: ans.append(i)
    print(*ans)

for _ in range(int(input())):
    solve()