def solve() -> None:
    n = sint()
    nums = ints()
    mx = 40 * n
    s, f = set(), set()
    for x in nums:
        g = set()
        for a in f:
            m = math.lcm(x, a)
            if m < mx:
                s.add(m)
                g.add(m)
        s.add(x)
        g.add(x)
        f = g
    ans = 1
    while ans in s: ans += 1
    print(ans)

for _ in range(int(input())):
    solve()