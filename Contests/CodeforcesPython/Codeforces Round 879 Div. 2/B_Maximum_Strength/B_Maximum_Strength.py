def solve() -> None:
    l, r = input().split()
    ln, rn = len(l), len(r)
    if ln < rn:
        l = "0" * (rn - ln) + l
    ans = 0
    for i, (a, b) in enumerate(zip(l, r)):
        if a != b:
            ans = int(b) - int(a) + 9 * (rn - 1 - i)
            break
    print(ans)


for _ in range(int(input())):
    solve()