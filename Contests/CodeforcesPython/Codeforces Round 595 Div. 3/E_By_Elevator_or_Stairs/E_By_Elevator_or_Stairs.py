def solve() -> None:
    n, c = mint()
    A = ints()
    B = ints()
    ans = [0] * n
    dp = [[0] * 2 for _ in range(n)]
    dp[0][1] = c
    for i, (a, b) in enumerate(zip(A, B), 1):
        dp[i][0] = min(dp[i - 1]) + a
        dp[i][1] = min(dp[i - 1][0] + b + c, dp[i - 1][1] + b)
        ans[i] = min(dp[i])
    # print(dp)
    print(*ans)

solve()