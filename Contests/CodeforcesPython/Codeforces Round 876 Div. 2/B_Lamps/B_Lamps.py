def solve() -> None:
    n = sint()
    lamp = []
    for _ in range(n):
        a, b = mint()
        lamp.append((a, b))
    lamp.sort(key = lambda x: (x[0], -x[1]))
    ans = cnt = pre = 0
    for a, b in lamp:
        if a == pre:
            cnt += 1
        else:
            cnt = 1
        pre = a
        if a >= cnt: ans += b
    print(ans)

for _ in range(int(input())):
    solve()