def solve() -> None:
    s = input()
    pos = sint()
    stk = []
    n = len(s)
    for i, c in enumerate(s):
        if pos <= n:
            stk.append(s[i:])
            break
        while stk and stk[-1] > c:
            stk.pop()
            pos -= n
            n -= 1
            if pos <= n: break
        if pos <= n:
            stk.append(s[i:])
            break
        stk.append(c)
    
    ans = "".join(stk)
    i = n - 1
    while pos > i + 1:
        pos -= i + 1
        i -= 1
    
    print(ans[pos - 1], end = "")

for _ in range(int(input())):
    solve()