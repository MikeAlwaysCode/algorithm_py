def solve() -> None:
    p = tuple(mint())
    a = tuple(mint())
    b = tuple(mint())
    z = (0, 0)
    
    def getDis(x, y):
        return math.sqrt(abs(x[0] - y[0]) ** 2 + abs(x[1] - y[1]) ** 2)
    
    ans = min(max(getDis(a, p), getDis(a, z)), max(getDis(b, p), getDis(b, z)))

    dis_ab = getDis(a, b) / 2
    dis_z = min(getDis(z, a), getDis(z, b))
    dis_p = min(getDis(p, a), getDis(p, b))
    ans = min(ans, max(dis_ab, dis_z, dis_p))

    print(ans)

for _ in range(int(input())):
    solve()