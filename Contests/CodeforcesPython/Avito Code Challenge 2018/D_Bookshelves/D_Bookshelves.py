def solve() -> None:
    n, k = mint()
    nums = ints()
    pres = list(itertools.accumulate(nums, initial = 0))
    ans = 0
    for bit in range(56, -1, -1):
        ans |= 1 << bit
        dp = [[False] * (n + 1) for _ in range(k + 1)]
        dp[0][0] = True
        for i in range(1, k + 1):
            for r in range(1, n + 1):
                for l in range(r):
                    dp[i][r] |= dp[i - 1][l] and ((pres[r] - pres[l]) & ans == ans)

        if not dp[k][n]: ans ^= 1 << bit
    
    print(ans)

solve()