    arr = ints()

    even = odd = 0
    cnt0 = cnt1 = 0
    for a in arr:
        if a & 1:
            cnt1 += 1
            odd += a
        else:
            cnt0 += 1
            even += a

    for _ in range(q):
        type, x = map(int, input().split())
        if type:
            odd += cnt1 * x
            if x & 1:
                cnt0 += cnt1
                cnt1 = 0
                even += odd
                odd = 0
        else:
            even += cnt0 * x
            if x & 1:
                cnt1 += cnt0
                cnt0 = 0
                odd += even
                even = 0

        print(odd + even)

