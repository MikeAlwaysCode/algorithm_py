def solve() -> None:
    n = sint()
    nums = ints()
    nums.sort()

    def check(x: int) -> bool:
        cnt = 1
        p = nums[0]
        for a in nums[1:]:
            if a - p > x * 2:
                cnt += 1
                p = a
        return cnt <= 3
    
    l, r = 0, nums[-1] - nums[0]
    while l < r:
        mid = (l + r) >> 1
        if check(mid):
            r = mid
        else:
            l = mid + 1
    print(r)

for _ in range(int(input())):
    solve()