def solve() -> None:
    n = sint()
    dp = [[-1] * 26 for _ in range(26)]
    for _ in range(n):
        s = input()
        l, r = ord(s[0]) - 97, ord(s[-1]) - 97
        for i in range(26):
            if dp[i][l] == -1: continue
            dp[i][r] = max(dp[i][r], dp[i][l] + len(s))
        dp[l][r] = max(dp[l][r], len(s))
    
    ans = max(dp[i][i] for i in range(26))
    print(0 if ans == -1 else ans)

solve()