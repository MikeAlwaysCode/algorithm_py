def solve() -> None:
    n, m = mint()
    g = []
    for _ in range(n):
        g.append(list(input()))

    floor = [n - 1] * m
    for i in range(n - 1, -1, -1):
        for j in range(m):
            if g[i][j] == 'o':
                floor[j] = i - 1
            elif g[i][j] == '*':
                g[i][j] = '.'
                g[floor[j]][j] = '*'
                floor[j] -= 1

    for row in g:
        print("".join(row))
    print()

for _ in range(int(input())):
    solve()