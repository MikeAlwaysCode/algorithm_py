def solve() -> None:
    n = sint()
    nums = ints()
    d = defaultdict(list)
    dp = [0] * (n + 1)
    for i, a in enumerate(nums):
        if not d[a]:
            for p in d[a]:
                dp[i + 1] = max(dp[i + 1], dp[p] + i - p + 1)
        d[a].append(i)
    for i in range(n):
        if i - dp[i] >= 0:
            dp[i] += dp[i - dp[i]]
        if i: dp[i] = max(dp[i], dp[i - 1])
    print(dp[-1])

for _ in range(int(input())):
    solve()