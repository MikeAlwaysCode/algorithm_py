def solve() -> None:
    s = list(map(int, list(input())))
    n = len(s)
    last = n
    for i in range(n - 1, 0, -1):
        if s[i] >= 5:
            s[i - 1] += 1
            last = i
    if s[0] >= 5:
        s = [1] + [0] * n
    else:
        for i in range(last, n):
            s[i] = 0
    print(*s, sep = "")

for _ in range(int(input())):
    solve()