def solve() -> None:
    n, k = mint()
    s = input()
    cnt = Counter(s)
    odd = list(v for v in cnt.values() if v & 1)
    print("YES" if k >= len(odd) - 1 else "NO")

for _ in range(int(input())):
    solve()