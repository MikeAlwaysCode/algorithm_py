def solve() -> None:
    n = sint()
    arr = ints()
    if n == 1:
        print(*arr)
        return

    i = arr.index(n - 1 if arr[0] == n else n)
    if (i == n - 1 and arr[i - 1] < arr[0]) or i == 1:
        ans = arr[i:] + arr[:i]
        print(*ans)
        return
    
    j = i - 2
    while j >= 0 and arr[j] > arr[0]:
        j -= 1
    
    ans = arr[i:] + arr[i - 1:j:-1] + arr[:j + 1]
    print(*ans)


for _ in range(int(input())):
    solve()