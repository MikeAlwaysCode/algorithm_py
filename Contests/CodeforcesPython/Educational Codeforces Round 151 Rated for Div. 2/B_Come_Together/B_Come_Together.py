def solve() -> None:
    xa, ya = mint()
    xb, yb = mint()
    xc, yc = mint()
    xb -= xa
    yb -= ya
    xc -= xa
    yc -= ya

    if xb * xc <= 0:
        mx = 0
    else:
        mx = min(abs(xb), abs(xc))
    if yb * yc <= 0:
        my = 0
    else:
        my = min(abs(yb), abs(yc))
    print(mx + my + 1)

for _ in range(int(input())):
    solve()